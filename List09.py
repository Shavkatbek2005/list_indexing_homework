    """
    A list of several elements is given. True if all items are the same, otherwise return False.
    Args:
        list1 (list): parameter
    Returns:
        bool: return answer
    """
    i=0
    while i<len(l):
        l[i]==l[i]

        i+=1
        return True
    else:
        return False
